package Week1;

public class dataTypes_2 {

    public static void main(String[] args) {

        // Integer
        int a = 4;
        System.out.println(a);
        // the variable name 'a' relates to a location in memory which holds the integer value 4
        // if we print 'a' we print 4

        // Double
        double b = 4.5;
        System.out.println(b);

        // String
        String c = "Hello"; // double quotations
        System.out.println(c);

        // Character can be a letter, number or symbol
        char d = 'x';
        char e = '3';
        char f = '&';
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);

        // Boolean - true or false
        boolean g = true;
    }
}
